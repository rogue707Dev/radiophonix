<template>
    <div>
        <router-view></router-view>

        <vue-progress-bar></vue-progress-bar>
    </div>
</template>

<script>
export default {
    mounted () {
        this.$Progress.finish();
    },
    created: function() {
        this.$Progress.start();

        this.$router.beforeEach((to, from, next) => {
            this.$Progress.start();

            next();
        });

        this.$router.afterEach((to, from) => {
            // finish the progress bar
            this.$Progress.finish();
        });
    }
}
</script>

